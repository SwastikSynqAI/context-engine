class TestEngine:
    def __init__(self, *, client: Any) -> None:
        self._client = client

    @retry(stop=stop_after_attempt(2), wait=wait_exponential(multiplier=1, min=2, max=8), reraise=False)
    async def _generate_module(self, *, prompt: str, module: str) -> list[dict] | None:
        try:
            message = await self._client.messages.create(
                model=CLAUDE_HAIKU_MODEL,
                max_tokens=2048,
                messages=[{"role": "user", "content": prompt}],
            )
            raw = message.content[0].text.strip()
            return json.loads(raw)
        except Exception as exc:
            logger.warning("Question generation failed for %s (%s), will use fallback.", module, exc)
            return None

    async def generate_questions(self, *, role: str, ai_required: bool) -> dict[str, list[dict]]:
        """Generate all test questions. Falls back to hardcoded bank if Claude unavailable."""
        aptitude_raw = await self._generate_module(prompt=_APTITUDE_PROMPT, module="aptitude")
        aptitude = aptitude_raw if aptitude_raw else _FALLBACK_APTITUDE

        english_raw = await self._generate_module(prompt=_ENGLISH_PROMPT, module="english")
        english = english_raw if english_raw else _FALLBACK_ENGLISH

        domain = _FALLBACK_DOMAIN.get(role, _FALLBACK_DOMAIN_DEFAULT)

        result: dict[str, list[dict]] = {"aptitude": aptitude, "english": english, "domain": domain}

        if ai_required:
            ai_raw = await self._generate_module(prompt=_AI_PROMPT, module="ai")
            if ai_raw:
                result["ai"] = ai_raw

        return result


def make_test_engine() -> TestEngine:
    import anthropic
    from src.config import get_settings
    client = anthropic.AsyncAnthropic(api_key=get_settings().anthropic_api_key)
    return TestEngine(client=client)
