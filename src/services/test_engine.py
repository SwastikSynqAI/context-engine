def generate_test_token() -> str:
    """Generate a cryptographically random URL-safe token."""
    return secrets.token_urlsafe(32)


def score_test(
    *,
    questions: dict[str, list[dict]],
    answers: dict[str, list[int]],
    ai_required: bool,
) -> dict[str, Any]:
    """
    Score a submitted test.

    Returns:
    - aptitude_score: 0-100
    - ai_score: 0-100 (None if not required)
    - english_score: 0-100
    - overall_score: 0-100 (weighted)
    - passed: bool
    - module_results: dict per module
    """
    def _score_module(module: str) -> float:
        qs = questions.get(module, [])
        ans = answers.get(module, [])
        if not qs:
            return 0.0
        correct = sum(1 for i, q in enumerate(qs) if i < len(ans) and ans[i] == q["correct_index"])
        return round(correct / len(qs) * 100, 1)

    aptitude = _score_module("aptitude")
    english = _score_module("english")
    domain_score = _score_module("domain") if "domain" in questions else None
    ai_score = _score_module("ai") if ai_required else None

    # Weights: aptitude 35%, english 30%, domain 35% (if present); else aptitude 55%, english 45%
    if domain_score is not None and ai_required and ai_score is not None:
        overall = round(aptitude * 0.30 + english * 0.25 + domain_score * 0.25 + ai_score * 0.20, 1)
    elif domain_score is not None:
        overall = round(aptitude * 0.35 + english * 0.30 + domain_score * 0.35, 1)
    elif ai_required and ai_score is not None:
        overall = round(aptitude * APTITUDE_WEIGHT + ai_score * AI_WEIGHT + english * ENGLISH_WEIGHT, 1)
    else:
        overall = round(aptitude * 0.55 + english * 0.45, 1)

    modules_pass = all([
        aptitude >= MODULE_PASS_THRESHOLD,
        english >= MODULE_PASS_THRESHOLD,
        (domain_score >= MODULE_PASS_THRESHOLD if domain_score is not None else True),
        (ai_score >= MODULE_PASS_THRESHOLD if ai_required and ai_score is not None else True),
    ])
    passed = overall >= PASS_THRESHOLD and modules_pass

    return {
        "aptitude_score": aptitude,
        "ai_score": ai_score,
        "english_score": english,
        "domain_score": domain_score,
        "overall_score": overall,
        "passed": passed,
        "module_results": {
            "aptitude": {"score": aptitude, "passed": aptitude >= MODULE_PASS_THRESHOLD},
            "english": {"score": english, "passed": english >= MODULE_PASS_THRESHOLD},
            **({"domain": {"score": domain_score, "passed": domain_score >= MODULE_PASS_THRESHOLD}} if domain_score is not None else {}),
            **({"ai": {"score": ai_score, "passed": ai_score >= MODULE_PASS_THRESHOLD}} if ai_required and ai_score is not None else {}),
        },
    }


class TestEngine:
    def __init__(self, *, client: Any) -> None:
        self._client = client

    @retry(stop=stop_after_attempt(3), wait=wait_exponential(multiplier=1, min=2, max=10), reraise=True)
    async def _generate_module(self, *, prompt: str, module: str) -> list[dict]:
        message = await self._client.messages.create(
            model=CLAUDE_HAIKU_MODEL,
            max_tokens=2048,
            messages=[{"role": "user", "content": prompt}],
        )
        raw = message.content[0].text.strip()
        try:
            return json.loads(raw)
        except json.JSONDecodeError:
            logger.error("Test generation JSON parse error for %s: %.200s", module, raw)
            return []

    async def generate_questions(self, *, role: str, ai_required: bool) -> dict[str, list[dict]]:
        """Generate all test questions. Returns dict keyed by module name."""
        aptitude = await self._generate_module(prompt=_APTITUDE_PROMPT, module="aptitude")
        english = await self._generate_module(prompt=_ENGLISH_PROMPT, module="english")

        result: dict[str, list[dict]] = {"aptitude": aptitude, "english": english}

        if ai_required:
            ai_qs = await self._generate_module(prompt=_AI_PROMPT, module="ai")
            result["ai"] = ai_qs

        return result


